const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const bannerValidation = require('../../validations/banner.validation');
const bannerController = require('../../controllers/banner.controller');

const router = express.Router();

router
  .route('/')
  .post(auth('createBanner'), validate(bannerValidation.createBanner), bannerController.createBanner)
  .get(auth('getBanners'), validate(bannerValidation.getBanners), bannerController.getBanners);

router
  .route('/active')
  .get(auth('getActiveBanners'), validate(bannerValidation.getActiveBanners), bannerController.getActiveBanners);

router
  .route('/:bannerId')
  .get(auth('getBanner'), validate(bannerValidation.getBanner), bannerController.getBanner)
  .put(auth('updateBanner'), validate(bannerValidation.updateBanner), bannerController.updateBanner)
  .delete(auth('deleteBanner'), validate(bannerValidation.deleteBanner), bannerController.deleteBanner);

module.exports = router;

