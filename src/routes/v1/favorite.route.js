const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const favoriteValidation = require('../../validations/favorite.validation');
const favoriteController = require('../../controllers/favorite.controller');

const router = express.Router();

router
  .route('/')
  .post(auth('createFavorite'), validate(favoriteValidation.createFavorite), favoriteController.createFavorite)
  .get(auth('getFavorites'), validate(favoriteValidation.getFavorites), favoriteController.getFavorites);

router
  .route('/all')
  .get(auth('getAllFavorites'), validate(favoriteValidation.getAllFavorites), favoriteController.getAllFavorites);

router
  .route('/:favoriteId')
  .get(auth('getFavorite'), validate(favoriteValidation.getFavorite), favoriteController.getFavorite)
  .put(auth('updateFavorite'), validate(favoriteValidation.updateFavorite), favoriteController.updateFavorite)
  .delete(auth('deleteFavorite'), validate(favoriteValidation.deleteFavorite), favoriteController.deleteFavorite);

module.exports = router;

