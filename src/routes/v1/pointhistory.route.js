const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const pointHistoryValidation = require('../../validations/pointhistory.validation');
const pointHistoryController = require('../../controllers/pointhistory.controller');

const router = express.Router();

router
  .route('/')
  .post(
    auth('createPointHistory'),
    validate(pointHistoryValidation.createPointHistory),
    pointHistoryController.createPointHistory
  )
  .get(
    auth('getPointHistories'),
    validate(pointHistoryValidation.getPointHistories),
    pointHistoryController.getPointHistories
  );

router
  .route('/:pointHistoryId')
  .get(auth('getPointHistory'), validate(pointHistoryValidation.getPointHistory), pointHistoryController.getPointHistory)
  .put(
    auth('updatePointHistory'),
    validate(pointHistoryValidation.updatePointHistory),
    pointHistoryController.updatePointHistory
  )
  .delete(
    auth('deletePointHistory'),
    validate(pointHistoryValidation.deletePointHistory),
    pointHistoryController.deletePointHistory
  );

module.exports = router;

