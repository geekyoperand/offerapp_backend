const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const cartValidation = require('../../validations/cart.validation');
const cartController = require('../../controllers/cart.controller');

const router = express.Router();

router
  .route('/')
  .post(auth('createCart'), cartController.createCart)
  .get(auth('getCarts'), validate(cartValidation.getCarts), cartController.getCarts)
  .put(auth('updateCart'), validate(cartValidation.updateCart), cartController.updateCart);

router.route('/me').get(auth('getMyCart'), validate(cartValidation.getMyCart), cartController.getMyCart);
router.route('/me/empty').put(auth('emptyCart'), validate(cartValidation.emptyCart), cartController.emptyCart);
router
  .route('/apply-wallet-cash/:applyCash')
  .post(auth('applyWalletCash'), validate(cartValidation.applyWalletCash), cartController.applyWalletCash);

router
  .route('/:cartId')
  .get(auth('getCart'), validate(cartValidation.getCart), cartController.getCart)
  .put(auth('updateCart'), validate(cartValidation.updateCart), cartController.updateCart)
  .delete(auth('deleteCart'), validate(cartValidation.deleteCart), cartController.deleteCart);

module.exports = router;

