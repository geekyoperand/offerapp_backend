const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const viewHistoryValidation = require('../../validations/viewhistory.validation');
const viewHistoryController = require('../../controllers/viewhistory.controller');

const router = express.Router();

router
  .route('/')
  .post(
    auth('createViewHistory'),
    validate(viewHistoryValidation.createViewHistory),
    viewHistoryController.createViewHistory
  )
  .get(auth('getViewHistories'), validate(viewHistoryValidation.getViewHistories), viewHistoryController.getViewHistories);

router
  .route('/:viewHistoryId')
  .get(auth('getViewHistory'), validate(viewHistoryValidation.getViewHistory), viewHistoryController.getViewHistory)
  .put(auth('updateViewHistory'), validate(viewHistoryValidation.updateViewHistory), viewHistoryController.updateViewHistory)
  .delete(
    auth('deleteViewHistory'),
    validate(viewHistoryValidation.deleteViewHistory),
    viewHistoryController.deleteViewHistory
  );

module.exports = router;

