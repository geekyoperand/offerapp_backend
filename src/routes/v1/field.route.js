const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const fieldValidation = require('../../validations/field.validation');
const fieldController = require('../../controllers/field.controller');

const router = express.Router();

router
  .route('/')
  .post(auth('createField'), validate(fieldValidation.createField), fieldController.createField)
  .get(auth('getFields'), validate(fieldValidation.getFields), fieldController.getFields);

router
  .route('/:fieldId')
  .get(auth('getField'), validate(fieldValidation.getField), fieldController.getField)
  .put(auth('updateField'), validate(fieldValidation.updateField), fieldController.updateField)
  .delete(auth('deleteField'), validate(fieldValidation.deleteField), fieldController.deleteField);

module.exports = router;

