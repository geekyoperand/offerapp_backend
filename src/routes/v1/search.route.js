const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const searchValidation = require('../../validations/search.validation');
const searchController = require('../../controllers/search.controller');

const router = express.Router();

router
  .route('/')
  .post(auth('createSearch'), validate(searchValidation.createSearch), searchController.createSearch)
  .get(auth('getSearches'), validate(searchValidation.getSearches), searchController.getSearches);

router
  .route('/:searchId')
  .get(auth('getSearch'), validate(searchValidation.getSearch), searchController.getSearch)
  .put(auth('updateSearch'), validate(searchValidation.updateSearch), searchController.updateSearch)
  .delete(auth('deleteSearch'), validate(searchValidation.deleteSearch), searchController.deleteSearch);

module.exports = router;

