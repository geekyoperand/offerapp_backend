const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const configurationValidation = require('../../validations/configuration.validation');
const configurationController = require('../../controllers/configuration.controller');

const router = express.Router();

router
  .route('/')
  .post(
    auth('createConfiguration'),
    validate(configurationValidation.createConfiguration),
    configurationController.createConfiguration
  )
  .get(
    auth('getConfigurations'),
    validate(configurationValidation.getConfigurations),
    configurationController.getConfigurations
  );

router
  .route('/all')
  .get(
    auth('getAllConfigurations'),
    validate(configurationValidation.getAllConfigurations),
    configurationController.getAllConfigurations
  );

router
  .route('/:configurationId')
  .get(
    auth('getConfiguration'),
    validate(configurationValidation.getConfiguration),
    configurationController.getConfiguration
  )
  .put(
    auth('updateConfiguration'),
    validate(configurationValidation.updateConfiguration),
    configurationController.updateConfiguration
  )
  .delete(
    auth('deleteConfiguration'),
    validate(configurationValidation.deleteConfiguration),
    configurationController.deleteConfiguration
  );

module.exports = router;

